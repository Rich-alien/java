public class arithoperation {
    public static void main (String [] args) {
        int a = 0x14D51;
        int b = 0x34A1;

        System.out.println("-:" + (a-b));
        System.out.println("+:" + (a+b));
        System.out.println("*:" + (a*b));
        System.out.println("/:" + (a/b));
        System.out.println("++:" + (a++));
        System.out.println("--:" + (b--));
    }
}